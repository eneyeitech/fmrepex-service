package com.example.fmrepexservice.business;

public class SignIn {
    private String email;
    private String pass;

    public SignIn(){}

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
